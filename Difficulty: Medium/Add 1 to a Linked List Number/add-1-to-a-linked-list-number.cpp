class Solution {
  public:
    int help(Node* head){
        if(!head)return 0;
        int n=1;
        if(head->next)n = help(head->next);
        head->data+=n;
        int r=head->data%10;
        int q=head->data/10;
        head->data=r;
        return q;
    }
    Node* addOne(Node* head) {
        if(help(head)){
            Node*newNode=new Node(1);
            newNode->next=head;
            return newNode;
        }
        return head;
    }
};
    
    
